package com.santidev;

public abstract class Sprite {

    private int x,y;

    public abstract void draw();

    public int getX(){
        return this.x;
    }

    public void setX(int x){
        this.x = x;
    }

    public int getY(){
        return this.y;
    }

    public void setY(int y){
        this.y = y;
    }
}
